# 基于波动率和涨跌幅计算情绪指数
# 简化计算：根据月度涨跌幅映射到情绪指数
def calc_sentiment(returns):
    """根据涨跌幅计算情绪指数 (0-100)"""
    # 1月暴跌 -> 恐慌
    # 3月反弹 -> 乐观
    # 其他月份根据涨跌幅调整
    sentiments = []
    for r in returns:
        if r <= -15:
            s = 5  # 极度恐慌
        elif r <= -5:
            s = 20  # 恐慌
        elif r <= 0:
            s = 35  # 谨慎
        elif r <= 3:
            s = 50  # 中性
        elif r <= 8:
            s = 70  # 乐观
        else:
            s = 85  # 狂热
        sentiments.append(s)
    return sentiments

